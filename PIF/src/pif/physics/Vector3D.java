package pif.physics;

public class Vector3D {
	
	private double x;
	private double y;
	private double z;
	
	public Vector3D () {
		
		this.x = 0;
		this.y = 0;
		this.z = 0;
		
	}
	
	public Vector3D (double x, double y, double z) {
		
		this.x = x;
		this.y = y;
		this.z = z;
		
	}
	

}
